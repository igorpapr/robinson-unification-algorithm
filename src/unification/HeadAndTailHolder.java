package unification;

public class HeadAndTailHolder {
    private final String head;
    private final String tail;

    public HeadAndTailHolder(String head, String tail) {
        this.head = head;
        this.tail = tail;
    }

    public String getHead() {
        return head;
    }

    public String getTail() {
        return tail;
    }
}
